'use strict';


// -------------------------------------------------------------------------------------------------------------------------------------------------------------
// Dependencies
// -------------------------------------------------------------------------------------------------------------------------------------------------------------
import Got from 'got';

// -------------------------------------------------------------------------------------------------------------------------------------------------------------
// Local
// -------------------------------------------------------------------------------------------------------------------------------------------------------------
import { Log } from './helper';


/**
 * GotData -
 *
 * @type {string} url     -
 * @type {object} options -
 */
export const GotData = ( url, options ) => {
	Log.verbose( `Got data from: ${ url }`);

	return new Promise( ( resolve, reject ) => {
		Got( url, options )
			.then( data => {
				resolve( data.body );
			})
			.catch( error => {
				reject( error )
			})
	})
};
